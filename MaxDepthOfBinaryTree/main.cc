/**
 * Definition for binary tree
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode(int x) : val(x), left(NULL), right(NULL) {}
 * };
 */
 
 int dep(TreeNode* A){
     return A==NULL ? 0 : max(dep(A->left), dep(A->right))+1;
 }
 
